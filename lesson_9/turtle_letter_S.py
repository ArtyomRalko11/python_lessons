from turtle import *

# Create a turtle for drawing
letter = Turtle()
letter.shape('turtle')
letter.color('#9e160d')
letter.pensize(7)

# Drawing the letter S
letter.lt(180)
letter.fd(99)
letter.lt(90)
letter.fd(99)
letter.lt(90)
letter.fd(99)
letter.rt(90)
letter.fd(99)
letter.rt(90)
letter.fd(99)

done()